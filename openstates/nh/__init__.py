    def get_organizations(self):
        legislature_name = "New Hampshire General Court"
        lower_chamber_name = "House"
        lower_title = "Representative"
        upper_chamber_name = "Senate"
        upper_title = "Senator"

        legislature = Organization(name=legislature_name,
                                   classification="legislature")
        upper = Organization(upper_chamber_name, classification='upper',
                             parent_id=legislature._id)
        lower = Organization(lower_chamber_name, classification='lower',
                             parent_id=legislature._id)

        for label, num, division_id in LOWER_DISTRICTS:
            lower.add_post(label=label, role=lower_title, division_id=division_id)

        for n in range(1, 25):
            upper.add_post(
                label=str(n), role=upper_title,
                division_id='{}/sldu:{}'.format(self.division_id, n))

        yield legislature
        yield upper
        yield lower

    def get_session_list(self):
        from openstates.utils import url_xpath
        zips = url_xpath('http://gencourt.state.nh.us/downloads/',
                         '//a[contains(@href, "Bill%20Status%20Tables")]/text()')
        return [zip.replace(' Bill Status Tables.zip', '') for zip in zips]


